# 4. WAP to calculate area of triangle and rectangle

lenght = float(input('Enter the lenght: '))
breadth = int(input('Enter the breadth: '))
height = int(input('Enter the height: '))


area_of_triangle = (breadth * height) * 0.5
area_of_rectangle = lenght * breadth

print('This is the area of traingle: ',area_of_triangle)
print('This is the area of reactangle: ',area_of_rectangle)
